

import 'package:flutter/material.dart';

class CustomPadding{
  get paddingPage => const EdgeInsets.all(16);
  get paddingHorizon => const EdgeInsets.symmetric(horizontal: 16);
  get paddingVertical => const EdgeInsets.symmetric(vertical: 16);
  get paddingTop => const EdgeInsets.only(top: 16);
  get paddingBottom => const EdgeInsets.only(bottom: 16);
  get itemPadding => const EdgeInsets.all(8);
  get itemPaddingHorizon => const EdgeInsets.symmetric(horizontal: 8);
  get itemPaddingVertical => const EdgeInsets.symmetric(vertical: 8);
}